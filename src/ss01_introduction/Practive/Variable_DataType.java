package ss01_introduction.Practive;

public class Variable_DataType {
    public static void main(String[] args) {
        int i = 10; //Khai báo biến i là kiểu int giá trị 10
        float f = 20.5f; // Khai báo biến f là kiểu float (số thực) có giá trị 20.5
        double d = 20.5; // Khai báo biến d là kiểu double có giá trị 20.5
        boolean b = true; // Khai báo biến b là kiểu boolean (true/false) có giá trị true
        char c = 'a'; // Khai báo biến c có kiểu char(ký tự) có giá trị là a
        String s = "Hà Nội"; //Khai báo s có kiểu là String có giá trị Hà Nội

        System.out.println("i + " + i);
        System.out.println("f + " + f);
        System.out.println("d + " + d);
        System.out.println("b + " + b);
        System.out.println("c + " + c);
        System.out.println("s + " + s);
    }
}
